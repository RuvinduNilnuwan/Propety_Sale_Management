    public void loadBlocks(){
        ResultSet rs=new DBSearch().searchblocks();
         DefaultTableModel dtm=(DefaultTableModel)tblLand1.getModel();
         DefaultTableModel dtm1=(DefaultTableModel)tblLand2.getModel();
        dtm.setRowCount(0);
        dtm1.setRowCount(0);
        try
        {
            Vector v=null;
            while(rs.next())
            {
                v=new Vector();
                v.add(rs.getString("BlockID"));
                v.add(rs.getString("BlockSizeAcres"));
                v.add(rs.getString("BlockSizeRoods"));
                v.add(rs.getString("BlockSizePerches"));
                v.add(rs.getString("PriceOfPerch"));
                v.add(rs.getString("BlockEstimatedValue"));
                v.add(rs.getString("BlockAvailability"));
                dtm.addRow(v);
                dtm1.addRow(v);
            }
             DBCon.closeCon();
            
        }
        catch(Exception k)
        {
            
        }   
    }
    
    public void LoadId(){
        DBSearch obj = new DBSearch();
        ResultSet rs = obj.LoadId();
        
        try{
            while(rs.next()){
                this.jComboBox1.addItem(rs.getString("LandID"));
            }
        }
        catch(Exception ex){
            System.out.println("Error"+ex.toString());
        }
    }
    
