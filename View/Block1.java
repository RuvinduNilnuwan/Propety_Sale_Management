    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        DBSearch obj = new DBSearch();
        DBSearch obj1 = new DBSearch();
        
        String LandId = jComboBox1.getSelectedItem().toString();
        
        ResultSet rs = obj.searchcount(LandId);
        ResultSet rs1=obj1. searchaproject(LandId);
        
        int flag=0; String Frq="";
        int flag1=0; String NumBlock="";
        
        try{
            while(rs.next())
            {
                Frq=rs.getString("Frequency");//DB table colum name

                flag=1;

                if(flag==1){
                    this.lblExceed.setText("This project has "+Frq+" blocks.");
                }

            }
        }catch(Exception k){
            System.out.println("error"+ k.toString());

        }

        try{
            while(rs1.next())
            {
                NumBlock=rs1.getString("NumberOfBlocks");//DB table colum name

                flag1=1;

                if(flag1==1){
                    int numberofblocks = Integer.parseInt(NumBlock);
                    int frequency = Integer.parseInt(Frq);
                    int remain = numberofblocks-frequency;
                    this.lblExceed.setText("Remaining Blocks: "+remain);
                    
                    if(numberofblocks<frequency){
                        this.lblExceed.setText("");
                        this.lblExceed.setText("Remaining Blocks: 0");
                   }
                    Numberofblocks=numberofblocks;
                    Frequency=frequency;
                }
            }
        }catch(Exception k){
            System.out.println("error"+ k.toString());

        }              
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

